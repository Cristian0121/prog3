    public void metodo(){
      a++;
        jLabel3.setText(String.valueOf(a));
        String colorImagen = getColorImagen();
        ImageIcon imagen1 = new ImageIcon(colorImagen);
        String numeroImagen = "1.png"; // Imagen est√°tica
        if (colorImagen.equals("rojo.png")) {
            numeroImagen = String.valueOf(contador % 8 + 1) + ".png";
        }
        ImageIcon imagen2 = new ImageIcon(numeroImagen);
        jLabel1.setIcon(imagen1);
        jLabel2.setIcon(imagen2);
        contador = (contador + 1) % 5;

       
    }

    private String getColorImagen() {
        if (a < 25 + tiempoExtraRojo) {
            return "rojo.png";
        } else if (a < 28 + tiempoExtraRojo) {
            return "amarillo.png";
        } else if (a < 48 + tiempoExtraRojo) {
            return "verde.png";
        } else {
            a = 0; 
            tiempoExtraRojo = 0;
            return "rojo.png";
        }
    }
   
